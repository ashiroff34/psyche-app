// ── Hash function: deterministic date + type → index ─────────────────────────

function hashDateType(dateKey: string, typeNumber: number): number {
  // Simple, deterministic djb2-style hash over the date string + type digit
  const str = `${dateKey}-${typeNumber}`;
  let hash = 5381;
  for (let i = 0; i < str.length; i++) {
    hash = (hash * 33) ^ str.charCodeAt(i);
    hash = hash >>> 0; // keep as unsigned 32-bit
  }
  return hash;
}

// ── Public API ────────────────────────────────────────────────────────────────

/**
 * Returns today's insight for a given Enneagram type.
 * Deterministic: all users of the same type see the same insight on the same day.
 * Cycles through 30 insights monthly.
 */
export function getDailyInsight(typeNumber: number): TypeInsight {
  const insights = insightsByType[typeNumber] ?? type1Insights;
  const dateKey = new Date().toISOString().slice(0, 10);
  const index = hashDateType(dateKey, typeNumber) % insights.length;
  return insights[index];
}
